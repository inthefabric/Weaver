using System;
using Weaver.Items;

namespace Weaver.Interfaces {

	/*================================================================================================*/
	public interface IWeaverRel : IWeaverItemWithId {


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		string Label { get; }

		/*--------------------------------------------------------------------------------------------*/
		WeaverRelConn Connection { get; set; }

		/*--------------------------------------------------------------------------------------------*/
		IWeaverRelType RelType { get; }
		bool IsFromManyNodes { get; }
		bool IsToManyNodes { get; }
		bool IsOutgoing { get; }

		/*--------------------------------------------------------------------------------------------*/
		Type FromNodeType { get; }
		Type ToNodeType { get; }

	}
	

	/*================================================================================================*/
	public interface IWeaverRel<out TFrom, out TTo> : IWeaverRel where TFrom : IWeaverNode
																			where TTo : IWeaverNode {


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		TFrom FromNode { get; }
		TTo ToNode { get; }

	}

}